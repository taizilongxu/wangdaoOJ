#include<stdio.h>
int main()
{
    int i,j,k,a,b;
    int c[4] = {0,0,0,0};
    while(scanf("%d %d",&a,&b) != EOF)
    {
        if(a <= 0 && b <= 0)
        {
            printf("%d %d %d %d\n",c[0],c[1],c[2],c[3]);
            for(i = 0; i < 4; i++)
                c[i] = 0;
        }
        if(a < c[0] || c[0] == 0) c[0] = a;
        if(a > c[2] || c[2] == 0) c[2] = a;
        if(b < c[1] || c[1] == 0) c[1] = b;
        if(b > c[3] || c[3] == 0) c[3] = b;
    }
}
